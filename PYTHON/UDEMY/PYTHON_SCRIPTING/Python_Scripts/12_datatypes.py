#BOOLEAN
'''
Here, if we don't put quotations on True or False with 
starting letter in CAPS, then, Python will take it as a BOOLEAN
which also means that if "True" or "False" is mentioned, then
it is a STRING
'''

print("Boolean:")
print("Example: 1")
my_value = True
my_new_value  = False
#my_one_more_value = true - ERROR
